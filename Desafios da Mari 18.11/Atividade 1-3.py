# 2- O que é uma variável, como ela deve ser escrita  e o que não pode ser utilizado em uma variável;
# Resposta: Uma váriavel é uma caixinha que vai armazenar a informação, e como o nome já diz varia de acordo com a informação que é guardada;
# Deve ser escrita de forma que facilite o entendimento de quem vai analisar o código e para nós mesmos, também por questões de boas práticas;
# Ela pode conter números mas NÃO pode começar com números.

# 3- O que é tupla: É um estrutura de armazenagem de dados que é imutável, ou seja não tem como alterar depois de feita;
# Lista: É um tipo de dado que pode conter difentes elementos, e pode ser alterada;
# Dicionário: É um tipo muito fácil de ser lido por suas informações ficarem bem estruturadas e explicativas.
# A diferença entre elas: Que uma é imutável e as outras não.

# Programa "Lista"
nome = ['Byanca', 'Thaize', 'Matheus', 'Natália' , 'Raiane']
print (type(nome))

# Programa "Dicionário"
altura = {'Carla': 1.65, 'Rafael': 1.60, 'Luara': 1.80,}
print(type(altura))

# Programa "Tupla"
valores =  (40, 48, 29, 98)
print(type(valores))




 




